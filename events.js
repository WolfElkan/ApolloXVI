function hms_to_millis(string) {
	if (!string) {
		return null
	}
	if (string[0] == '-') {
		return -hms_to_millis(string.substring(1))
	}
	match = string.match(/^(\d{3}):(\d\d)(:(\d\d(\.\d+)?))?/)
	if (match) {	
		hour   = match[1]
		minute = match[2]
		second = match[4]
	}
	match = string.match(/^(\d{3}):(\d\d)/)
	if (match) {	
		hour   = match[1]
		minute = match[2]
		second = 0
	}
	return hour * HOUR + minute * MINUTE + second * SECOND
	match = string.match(/.*/)
	if (match) {
		return -802
	}
}


