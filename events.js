function audio_to_millis(title) {
	var result
	if (!title) {
		result = null
	}
	match = title.match(/^a\d+_(t-)?(\d\d\d)(\d\d)(\d\d)(-hold\d\d|-cm)?/)
	if (match) {	
		hour   = match[2]
		minute = match[3]
		second = match[4]
		result = hour * HOUR + minute * MINUTE + second * SECOND
		if (match[1]) {
			result *= -1
		}
	} else {
		result = NaN
	}
	// console.log(title, result, match[5])
	return result
}

// Superfluous, does the same thing as hms_to_millis
function GET_to_millis(get) {
   // console.log(get)
   match = get.match(/^(-?)(\d{3,4}):(\d\d)(:(\d\d(\.\d+)?))?$/)
   // console.log(match)
   let n = match[1]
   let h = Number(match[2])
   let m = Number(match[3])
   let s = Number(match[5] ? match[5] : 0)
   let total = h * HOUR + m * MINUTE + s * SECOND
   if (n == '-') {
      total *= -1
   }
   return total
}

